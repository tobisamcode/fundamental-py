# Functions for Strings
s = "welcome"
print(len(s)) # 7
print(max(s)) # o
print(min(s)) # W
'''Since s has 7 characters, len(s) returns 7 (line 3). Note that the lowercase letters have a
higher ASCII value than the uppercase letters, so max(s) returns o (line 5) and min(s)
returns W'''